        const defaultCode = '// React Hook Á§∫‰æã\\nfunction Counter() {\\n    const [count, setCount] = useState(0);\\n    \\n    return (\\n        <div>\\n            <p>Count: {count}</p>\\n            <button onClick={() => setCount(count + 1)}>\\n                Increment\\n            </button>\\n        </div>\\n    );\\n}';

